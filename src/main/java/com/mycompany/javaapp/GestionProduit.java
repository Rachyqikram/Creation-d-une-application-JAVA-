    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String numpd = jTextField1.getText();
        String nom = jTextField2.getText();
        String quantite = jTextField3.getText();
        String prix = jTextField4.getText();
      
        c = new Produits(Integer.parseInt(numpd),nom,Integer.parseInt(quantite),Double.parseDouble(prix));
        pd.ajouter(c); // add data to MySQL
        JOptionPane.showMessageDialog(this, "Produit ajouté");
        Object[] data = {numpd,nom,quantite,prix};
        model.addRow(data);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel dt =(DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
         int numpd = Integer.parseInt(dt.getValueAt(selected, 0).toString());
        
        String nom = jTextField2.getText();
        String quantite = jTextField3.getText();
        String prix = jTextField4.getText();
      
        c = new Produits(numpd,nom,Integer.parseInt(quantite),Double.parseDouble(prix));
        pd.modifier(c); //modifier le client dans MySQL
        JOptionPane.showMessageDialog(this, "Produit modifié");
        dt.removeRow(selected);
        dt.addRow(new Object[]{numpd,nom,quantite,prix});
          jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton5ActionPerformed

