    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String numf = jTextField1.getText();
        String date = jTextField2.getText();
        String montant = jTextField3.getText();
        c = new Facture(Integer.parseInt(numf),LocalDate.parse(date, formatter),Double.parseDouble(montant));
        f.ajouter(c); // add data to MySQL
        JOptionPane.showMessageDialog(this, "Facture ajouté");
        Object[] data = {numf,date,montant};
        model.addRow(data);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField1.requestFocus();
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         DefaultTableModel dt =(DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
         int numf = Integer.parseInt(dt.getValueAt(selected, 0).toString());
        String date = jTextField2.getText();
        String montant = jTextField3.getText();
        c = new Facture(numf,LocalDate.parse(date, formatter),Double.parseDouble(montant));
        f.modifier(c); //modifier le client dans MySQL
        JOptionPane.showMessageDialog(this, "Facture modifié");
        dt.removeRow(selected);
        dt.addRow(new Object[]{numf,date,montant});
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton2ActionPerformed

