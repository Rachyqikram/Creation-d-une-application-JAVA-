    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String numlv = jTextField1.getText();
        String date = jTextField2.getText();
        
        c = new Livraison(Integer.parseInt(numlv),LocalDate.parse(date, formatter));
        cm.ajouter(c); // add data to MySQL
        JOptionPane.showMessageDialog(this, "Livraison ajouté");
        Object[] data = {numlv,date};
        model.addRow(data);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel dt =(DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
         int numlv = Integer.parseInt(dt.getValueAt(selected, 0).toString());
        String date = jTextField2.getText();
        c = new Livraison(numlv,LocalDate.parse(date, formatter));
        cm.modifier(c); //modifier le client dans MySQL
        JOptionPane.showMessageDialog(this, "Livraison modifié");
        dt.removeRow(selected);
        dt.addRow(new Object[]{numlv,date});
          jTextField1.setText("");
        jTextField2.setText("");
        
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton2ActionPerformed

